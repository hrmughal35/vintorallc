// Get unique country codes (some countries share codes)
export const getUniqueCountryCodes = () => {
  const seen = new Set()
  return countryCodes.filter(item => {
    const key = `${item.code}-${item.country}`
    if (seen.has(key)) {
      return false
    }
    seen.add(key)
    return true
  }).sort((a, b) => a.country.localeCompare(b.country))
}
